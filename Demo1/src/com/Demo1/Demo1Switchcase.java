package com.Demo1;

public class Demo1Switchcase {
	public static void main(String[] args) {
	String city="Mumbai";
		switch(city) {
		case "Mumbai":
			System.out.println("Excellent");
			break;
		case "Chennai":
			System.out.println("Very Good");
			break;
		default:
			System.out.println("Average");
		}
	}
}
